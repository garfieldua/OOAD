package com.vateam.rental;

import java.util.Date;

public class DateRange {

	private Date starts;
	private Date ends;
	
	public Date getStarts() {
		return starts;
	}
	
	public void setStarts(Date starts) {
		this.starts = starts;
	}
	
	public Date getEnds() {
		return ends;
	}
	
	public void setEnds(Date ends) {
		this.ends = ends;
	}
	
}
